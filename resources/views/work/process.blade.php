      <div class="process-narrative">
      <ul class="process-list">
        <li id="li-1">
          <h2>Gather & Analyze Information</h2>
          <p>Become intimately familiar with the tool, product, or feature and ask questions.
            Find out what the wins are for the business and stakeholders. Learn about
            the business's competition if it exists and their differences and similarities.</p>
        </li>
        <li id="li-2">
          <h2>Consider User Input</h2>
          <p>Areas of opportunity identified at this stage present a string of evidence
            that support the need for improvements that the current state of a product
            or tool may lack.</p>
        </li>
        <li id="li-3">
          <h2>Frame Feedback</h2>
          <p>Organizing at this level of development usually helps in charting the bottlenecks
            in productivity and may help establish time frames or deadlines levied on
            business-critical updates.</p>
        </li>
        <li id="li-4">
          <h2>Brainstorm Solutions</h2>
          <p>Partner with team members and end-users to architect solutions for an identified
            improvement needed. Outlining a skeleton of the goals within a conversation
            provides a road map for forward movement.</p>
        </li>
        <li id="li-5">
          <h2>Map Ideas</h2>
          <p>Construct scenarios for proposed solutions while exploring the scope of impact
            that each change will incur in a process's current implementation.</p>
        </li>
        <li id="li-6">
          <h2>Build & Test Prototype</h2>
          <p>Once an initial, rapid prototype of any changes are conceived, gather the
            perspectives of different users by beta testing and collecting positive and
            negative feedback.</p>
        </li>
        <li id="li-7">
          <h2>Refine Until Deployment</h2>
          <p>With each iteration of the prototype, filter and factor in collected feedback
            to make each version released better than the last. Cycle through versions and refine
            the best parts of each until the solution is deemed ready for deployment.</p>
        </li>
      </ul>
